import UIKit

extension UIColor {
    class func treeGreenColor() -> UIColor {
        return UIColor(red: 130/255.0, green: 212/255.0, blue: 53/255.0, alpha: 1.0)
    }
    
    class func goldColor() -> UIColor {
        return UIColor(red: 255/255.0, green: 208/255.0, blue: 0/255.0, alpha: 1.0)
    }
}
